namespace MyApp
{
    class Program
    {
        static void Main(string[] args)
        {
            // all type variable 
            // int, double, string, bool, char, byte, short, long, float, decimal
            int a = 10;
            double b = 10.5;
            string c = "Hello";
            bool d = true;
            char e = 'a';
            byte f = 10;
            short g = 10;
            long h = 10;
            float i = 10.5f;
            decimal j = 10.5m;
            // writeLine all type variable
            Console.WriteLine($"{a} {b} {c} {d} {e} {f} {g} {h} {i} {j}");
        }
    }
}